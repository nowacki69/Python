# # NavigableString
# # A string corresponds to a bit of text within a tag. Beautiful Soup uses the
# # NavigableString class to contain these bits of text:
# print("NavigableString")
# print(f"1.  {tag.string}")						# u'Extremely bold'
# print(f"2.  {type(tag.string)}")				# <class 'bs4.element.NavigableString'>
# print()
# print()

# # A NavigableString is just like a Python Unicode string, except that it also
# # supports some of the features described in Navigating the tree and Searching
# # the tree. You can convert a NavigableString to a Unicode string with unicode():
# # Python 3 renamed the unicode type to str, the old str type has been replaced by bytes.
# unicode_string = str(tag.string)
# print(f"3.  {unicode_string}")				# 'Extremely bold'
# print(f"4.  {type(unicode_string)}")			# <class 'str'>
#
# # You can’t edit a string in place, but you can replace one string with another,
# # using replace_with():
# tag.string.replace_with("No longer bold")
# print(tag)									# <blockquote>No longer bold</blockquote>
# print()
# print()
#
#
#
# # The BeautifulSoup object itself represents the document as a whole. For most
# # purposes, you can treat it as a Tag object. This means it supports most of
# # the methods described in Navigating the tree and Searching the tree.
#
# # Since the BeautifulSoup object doesn’t correspond to an actual HTML or XML
# # tag, it has no name and no attributes. But sometimes it’s useful to look at
# # its .name, so it’s been given the special .name “[document]”
# print("BeautifulSoup")
# print(soup.name)								# [document]
# print()


# # Comments and other special strings
#
# # Tag, NavigableString, and BeautifulSoup cover almost everything you’ll see
# # in an HTML or XML file, but there are a few leftover bits. The only one
# # you’ll probably ever need to worry about is the comment:
# markup = "<b><!--Hey, buddy. Want to buy a used parser?--></b>"
# soup = BeautifulSoup(markup, 'html.parser')
# comment = soup.b.string
# print(f"1.  {type(comment)}")					# <class 'bs4.element.Comment'>
#
# # The Comment object is just a special type of NavigableString:
# print(f"2.  {comment}")							# u'Hey, buddy. Want to buy a used parser'
#
# # But when it appears as part of an HTML document, a Comment is displayed with special formatting:
# print(f"3.  {soup.b.prettify()}")					# <b>
# 													#  <!--Hey, buddy. Want to buy a used parser?-->
# 													# </b>
# print()
#
# # Beautiful Soup defines classes for anything else that might show up in an
# # XML document: CData, ProcessingInstruction, Declaration, and Doctype. Just
